package nct.auction.port;

/**
 * [담당자4(거래) 서비스 계약 - 담당자3이 "소비"하는 인터페이스 (신규 제안)]
 *
 * - TRADE 테이블의 고정 기술 소유자는 담당자4 이다. F-AUC-018(즉시구매 실행)이 성공하면
 *   "경매 종료 -> 거래진행 상태로 전환"이 일어나야 하는데, 이건 TRADE 행 생성을 의미한다.
 * - 7.1절 규칙: "경매·견적 담당자는 TRADE 를 직접 쓰지 않고 담당자4의 거래 생성 계약을 호출한다."
 *   따라서 담당자3은 TRADE 에 직접 INSERT 하지 않고, 이 인터페이스를 통해서만 거래를 만든다.
 *
 * ------------------------------------------------------------------------------
 * [병합 전 담당자4와 반드시 맞춰야 하는 것 - 09번 업무분장 "TRADE 유형·참조 조합 검증" 항목]
 * ------------------------------------------------------------------------------
 * 09_기능단위_7인_업무분장 최신본(v4) 10.1절에 "TRADE 유형·참조 조합 검증"이 아직
 * 담당자4 주도로 결정되지 않은 미결정 항목으로 남아있다. 즉, TRD_TYPE_CD 에 어떤 값을 넣을지,
 * PRD_SN 외에 AUC_SN 을 TRADE 에 별도로 남길지 등은 담당자4가 결정할 몫이라
 * 이 인터페이스는 "담당자3이 확실히 아는 최소 정보"만 넘기고 나머지는 담당자4 구현체 안에서
 * PRODUCT 조회 등을 통해 스스로 채우도록 설계했다.
 *   1) TRD_TYPE_CD 값 후보(예: 경매 낙찰 vs 즉시구매를 같은 코드로 볼지 다르게 볼지)
 *   2) TRADE 에 AUC_SN 을 남길 별도 컬럼이 필요한지 (현재 DDL 에는 PRD_SN 만 있고 AUC_SN 없음)
 *   3) 이 메서드가 BidService 의 트랜잭션에 참여하는지 (참여해야 "경매 종료 + 거래 생성"이 원자적)
 * ------------------------------------------------------------------------------
 */
public interface TradeCreationPort {

    /**
     * 즉시구매 성공으로 거래를 1건 생성한다.
     * @param aucSn        종료된 경매 번호 (참고용 - TRADE 자체 컬럼은 아닐 수 있음, 담당자4가 판단)
     * @param prdSn        상품 번호
     * @param sellerUsrSn  판매자 회원번호
     * @param buyerUsrSn   구매자(즉시구매자) 회원번호
     * @param finalAmt     최종 거래금액 (= 즉시구매가)
     * @return 생성된 TRADE.TRD_SN
     */
    Long createTradeForBuyNow(Long aucSn, Long prdSn, Long sellerUsrSn, Long buyerUsrSn, Long finalAmt);
}
