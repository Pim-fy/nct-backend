package nct.auction.port;

/**
 * [담당자6(포인트 원장) 서비스 계약 - 담당자3이 "소비"하는 인터페이스]
 * - POINT_LEDGER 테이블의 고정 기술 소유자는 담당자6 이다.
 * - 담당자3은 "보유 포인트가 충분한지"만 알면 되고, 원장을 직접 읽거나 쓰지 않는다
 *   (7.1절 규칙: "입찰 담당자는 POINT_LEDGER 를 직접 쓰지 않고 담당자6의 홀딩/반환 계약을 호출한다").
 *
 * ------------------------------------------------------------------------------
 * [F-AUC-014 구현을 위해 계약을 2개 추가함 - 담당자6과 시그니처 협의 필요]
 * ------------------------------------------------------------------------------
 * - holdForBid(), releaseHold() 는 013 시점에는 없던 새 메서드다.
 *   F-AUC-015(입찰 포인트 홀딩), F-AUC-016(기존 최고 입찰 반환)은 담당자6 소유 기능이지만,
 *   "언제 호출되어야 하는지"는 입찰 실행 흐름(F-AUC-014) 안에서 결정되므로
 *   담당자3이 필요한 시그니처를 먼저 제안하는 형태로 추가했다.
 * - 실제 병합 전에 반드시 담당자6과 아래를 맞춰야 한다.
 *     1) bidSn 을 원장 참조키로 남길지 (반환 시점에 "어떤 입찰 때문에 홀딩했는지" 추적 목적)
 *     2) 홀딩/반환이 실패했을 때 예외를 던질지, boolean 으로 알릴지
 *     3) 이 메서드가 자체 트랜잭션인지, BidService 의 트랜잭션에 참여(propagation)하는지
 *        - 참여해야 "BID insert 성공 + 포인트 홀딩 성공"이 하나의 원자적 단위가 된다.
 * ------------------------------------------------------------------------------
 */
public interface PointLedgerPort {

    /**
     * 사용 가능 포인트 잔액이 입찰 금액 이상인지 확인한다.
     * @param usrSn  확인할 회원 번호
     * @param amount 입찰하려는 금액
     * @return true = 입찰 가능, false = 잔액 부족
     */
    boolean hasAvailableBalance(Long usrSn, Long amount);

    /**
     * [신규 제안 - F-AUC-015] 입찰 금액만큼 "사용 가능"에서 "홀딩"으로 옮긴다.
     * @param usrSn 입찰자 회원 번호
     * @param amount 홀딩할 금액 (= 입찰 금액)
     * @param bidSn  이 홀딩이 어떤 입찰 때문에 생겼는지 추적하기 위한 참조키
     */
    void holdForBid(Long usrSn, Long amount, Long bidSn);

    /**
     * [신규 제안 - F-AUC-016] 더 높은 입찰에 밀려난 이전 최고 입찰자의 홀딩을 즉시 반환한다.
     * @param usrSn  환불받을 이전 최고 입찰자 회원 번호
     * @param amount 반환할 금액 (= 그 사람이 홀딩했던 입찰 금액)
     * @param bidSn  반환 대상이 되는 이전 입찰 번호 (감사/추적용)
     */
    void releaseHold(Long usrSn, Long amount, Long bidSn);
}
